package sec01_datatype.Ex02_NamingVariableAndConstant;

public class NamingVariableAndConstant {
	public static void main(String[] args) {
		//변수
		boolean aBcD;	// 새로운 문자 시작은 대문자로
		byte 가나다;		// 한글로 변수명 작성 가능하나 권장하지 않음
		short _abcd;
		char $ab_cd;	// 특수문자 $ 사용가능하며 첫 시작으로 사용 가능
		// int 3ab;		// 숫자는 앞에 올 수 없음
		long abcdf3;	// 뒤에오는 숫자는 가능
		// float int;	// 자바 예약어는 변수명으로 사용 불가능
		double main;	
		// int my world;	// 스페이스, 특수 키는 사용 불가
		String myClassName;
		int ABC;		// 전부 대문자로 작성(권하지 않음)
		
		// 상수
		final double PI;	// final 자료형 변수병
		final float myData;	// 소문자사용(권장하지 않음)
		
		
	}

}
